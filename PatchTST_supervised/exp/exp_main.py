    def load_trained_model(self, setting):
        """
        Carrega o checkpoint treinado (checkpoint.pth) para self.model.
        """
        ckpt_path = os.path.join(self.args.checkpoints, setting, 'checkpoint.pth')
        if not os.path.exists(ckpt_path):
            raise FileNotFoundError(f"Checkpoint não encontrado: {ckpt_path}")

        state = torch.load(ckpt_path, map_location=self.device)
        self.model.load_state_dict(state)
        self.model.eval()
        return self.model

    def predict_one_step_from_history(self, model, window_df, fut_row, futr_exog_list):
        """
        Constrói X a partir das últimas seq_len linhas (window_df) e executa um forward.
        Retorna um único valor previsto (float).
        """
        import numpy as np
        import torch

        # determina colunas
        date_col = 'date' if 'date' in window_df.columns else window_df.columns[0]
        target = self.args.target
        exogs = [c for c in window_df.columns if c not in [date_col, target]]

        # aplica filtro de exógenas, se necessário
        if futr_exog_list is not None:
            exogs = [c for c in exogs if c in futr_exog_list]

        cols = exogs + [target]

        # shape [L, C]
        X = window_df[cols].values.astype(np.float32)

        # PatchTST usa entrada [B, L, C]
        X = np.expand_dims(X, axis=0)
        X = torch.tensor(X).float().to(self.device)

        with torch.no_grad():
            out = model(X)

        # Saída PatchTST: [B, pred_len, c_out]
        y_pred = out[:, -1, 0].item()
        return float(y_pred)

    def walk_forward_and_save(self, setting, out_dir, futr_exog_list=None):
        """
        Walk-forward sem retreino.
        Usa o modelo treinado (checkpoint) e prevê 1 passo por vez.
        Salva previsões contínuas e métricas agregadas.
        """
        import pandas as pd
        import numpy as np
        import os
        import time  # Adicionar import do time

        os.makedirs(out_dir, exist_ok=True)

        # carregar dataset completo usado no experimento
        data_path = os.path.join(self.args.root_path, self.args.data_path)
        df = pd.read_csv(data_path)

        date_col = 'date' if 'date' in df.columns else df.columns[0]
        target = self.args.target

        # mesmo split da classe Dataset_Custom: 70/20/10
        n = len(df)
        train_end = int(n * 0.7)
        test_start = int(n * 0.8)

        # histórico inicial
        history = df.iloc[:train_end].copy().reset_index(drop=True)

        # carregar modelo treinado
        model = self.load_trained_model(setting)
        model.eval()

        preds = []
        
        # MEDIR TEMPO DE EXECUÇÃO DO WALK-FORWARD
        start_time = time.time()

        # loop do walk-forward
        for i in range(train_end, n):
            fut_row = df.iloc[i]

            # janela final
            window = history.iloc[-self.args.seq_len:].copy()

            try:
                y_pred = self.predict_one_step_from_history(
                    model,
                    window,
                    fut_row,
                    futr_exog_list
                )
            except:
                # fallback persistente
                y_pred = float(window[target].iloc[-1])

            y_true = float(fut_row[target])

            mse = (y_true - y_pred) ** 2
            mae = abs(y_true - y_pred)
            mape = abs(y_true - y_pred) / y_true * 100 if y_true != 0 else 0

            preds.append({
                "date": fut_row[date_col],
                "real": y_true,
                "pred": y_pred,
                "mse": mse,
                "mae": mae,
                "mape": mape
            })

            # atualiza histórico
            history = pd.concat([history, fut_row.to_frame().T], ignore_index=True)

        # CALCULAR TEMPO TOTAL E MÉDIO POR PREDIÇÃO
        end_time = time.time()
        total_time_seconds = end_time - start_time
        total_time_minutes = total_time_seconds / 60.0
        avg_time_per_prediction = total_time_seconds / len(preds) if preds else 0

        # salvar previsões
        pred_path = os.path.join(out_dir, f"{setting}_walkforward_predictions.csv")
        pd.DataFrame(preds).to_csv(pred_path, index=False)

        # métricas agregadas (INCLUINDO TEMPOS)
        dfp = pd.DataFrame(preds)
        metrics = {
            "MSE": dfp["mse"].mean(),
            "MAE": dfp["mae"].mean(),
            "MAPE": dfp["mape"].mean(),
            "n": len(dfp),
            "total_segundos": total_time_seconds,
            "total_minutos": total_time_minutes,
            "media_predição": avg_time_per_prediction
        }

        metrics_path = os.path.join(out_dir, f"{setting}_walkforward_metrics.csv")
        pd.DataFrame([metrics]).to_csv(metrics_path, index=False)

        print(f"Walk-forward concluído em {total_time_minutes:.2f} minutos")
        print(f"Tempo médio por predição: {avg_time_per_prediction:.4f} segundos")
        print("Walk-forward salvo em:", out_dir)
        return pred_path, metrics_path